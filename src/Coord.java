public class Coord {

    private int xValue;
    private int yValue;

    Coord(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public int x() {
        return xValue;
    }

    public int y() {
        return yValue;
    }
}
